void main() {
  int a = 5;
  int b = 10;

  // Operasi perkalian
  int hasilPerkalian = a * b;
  print('$a * $b = $hasilPerkalian');

  // Operasi penjumlahan
  int hasilPenjumlahan = a + b;
  print('$a + $b = $hasilPenjumlahan');

  // Operasi pengurangan
  int hasilPengurangan = a - b;
  print('$a - $b = $hasilPengurangan');

  // Operasi pembagian
  double hasilPembagian = a / b; // Menggunakan tipe data double untuk hasil pembagian
  print('$a / $b = $hasilPembagian');
}
